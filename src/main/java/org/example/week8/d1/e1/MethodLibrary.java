package org.example.week8.d1.e1;

public class MethodLibrary {
    /* 1. ** `MethodLibrary` Class**
   - Inside this class, create two methods: `greet` and `farewell`.
   - The `greet` method should print `Hello, welcome to Java!`,
    and the `farewell` method should print `Goodbye! Have a great day!`.     */
    public void greet() {
        System.out.println("Hello, welcome to Java!");
    }
    public void farewell() {
        System.out.println("Goodbye! Have a great day!");
    }

}
