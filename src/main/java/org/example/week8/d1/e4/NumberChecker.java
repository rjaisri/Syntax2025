package org.example.week8.d1.e4;

public class NumberChecker {
    // Create Method isEven
    // - Create a method named isEven that accepts one parameter of integer type: number.
    public boolean isEven(int number) {
        // - Inside the method body, return true if the number is even, otherwise return false.
        return number % 2 == 0;
    }
}
